package com.go.spring.model;

public class TsMenuEx extends BaseModel {

	private Integer menuExId = null;
	private String menuId = null;
	private String menuName = null;
	private String menuImage = null;
	private String menuUrl = null;
	
	public Integer getMenuExId() {
		return menuExId;
	}
	public void setMenuExId(Integer menuExId) {
		this.menuExId = menuExId;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuImage() {
		return menuImage;
	}
	public void setMenuImage(String menuImage) {
		this.menuImage = menuImage;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	
	
	
}
